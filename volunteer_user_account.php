         }
         if (!empty($phone)) {
             $result = $wpdb->query(" UPDATE wp_user_details SET phone='$phone' WHERE ID='$user_id' " );
         }         

         if (!$result) {
             wp_die('Database update failed');
         }
         echo "Update successful. user ID: ". $user_id;       
