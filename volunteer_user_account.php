 function update($username,$pass,$email,$gender,$dob,$nationality,$phone,$user_id){

         global $wpdb;

         if (!empty($username)) {
             $result = $wpdb->query(" UPDATE wp_user_details SET userName= '$username' WHERE ID='$user_id' " );
         }
         if (!empty($pass)) {
             $result = $wpdb->query(" UPDATE wp_user_details SET password='$pass' WHERE ID='$user_id' " );
         }
         if (!empty($email)) {
             $result = $wpdb->query(" UPDATE wp_user_details SET email='$email' WHERE ID='$user_id' " );
